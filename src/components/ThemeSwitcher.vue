<template>
  <label ref="themeSwitch" id="theme-switch" class="theme-switch">
    <input type="checkbox" id="theme-switch-input" />
    <span class="slider">
    </span>
  </label>
</template>

<script>
import { ref, onMounted } from 'vue'

export default {
  name: 'ThemeSwitcher',

  setup() {
    const themeSwitch = ref('')

    function toogleTheme() {
      const elements = document.querySelectorAll('*[data-bs-theme]')
      elements.forEach(element => {
        const theme = element.getAttribute('data-bs-theme') === 'light' ? 'dark' : 'light';
        element.setAttribute('data-bs-theme', theme)
      });
    }

    onMounted(() => {
      themeSwitch.value.addEventListener("change", toogleTheme, false);
    })

    return { themeSwitch }
  }
}
</script>

